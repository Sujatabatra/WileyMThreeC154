package com.sujata.demo;

public class MyDemoClass {

	public static void main(String[] args) {
		/*
		 * keywords, identifiers, literals
		 * keywords : are the reserved words of the programming language
		 * identifiers : names of classes, methods, variables etc.
		 * literals : any constant values 
		 * 
		 * e.g int i=10; int : keyword, i: identifier, 10: literal
		 */
		
		int x=(int)10L;
		
		long y=10;
		
		float a=8.9f;
		
//		int i=10;
//		int i=010;
//		int i=0x10;
		int i=0b10;
		System.out.println("i : "+i);
		

	}

}
